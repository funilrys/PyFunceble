from sqlalchemy import Column, Integer, Text

from PyFunceble.database.sqlalchemy.base_schema import SchemaBase


class WhoisRecord(SchemaBase):
    """
    Provides the schema of our whois_record table.
    """

    subject = Column(Text, nullable=False, unique=True)
    idna_subject = Column(Text, nullable=True, unique=False)
    expiration_date = Column(Text, nullable=False)
    epoch = Column(Integer, nullable=False)
    registrar = Column(Text, nullable=True)
