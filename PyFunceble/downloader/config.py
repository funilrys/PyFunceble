import PyFunceble

from .base import DownloaderBase


class ConfigDownloader(DownloaderBase):
    """
    Provides the dowloader of the production configuration file.
    """

    DOWNTIME_INDEX = "config"
    REDOWNLOAD_AFTER = 0

    def __init__(self):
        self.destination = (
            f"{PyFunceble.CONFIG_DIRECTORY}"
            f"{PyFunceble.abstracts.Infrastructure.DEFAULT_CONFIGURATION_FILENAME}"
        )

        if (
            not PyFunceble.abstracts.Version.is_local_cloned()
            or not PyFunceble.helpers.File(self.destination).exists()
        ):
            self.download_link = PyFunceble.converter.InternalUrl(
                PyFunceble.abstracts.Infrastructure.PROD_CONFIG_LINK
            ).get_converted()

            super().__init__()

            self.process()
